#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
		ft_putnbr(nb);
	}
	else if (nb < 10)
		write(1, &(char){nb + 48}, 1);
	else if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		write(1, &(char){nb % 10 + 48}, 1);
	}
}
